	/**
	 * the format returned is property:value
	 * @param value
	 * @return
	 */
	public String getCSSElementWithAttributeValue(String value){
		String s = String.format("%s:%s", toString(),value);
		return s;
	}
	
	@Override
	/**
	 * the format returned is lower case and for instances of _ are replaces with a -
	 * and instances of $ are replaced with a &amp
	 */
	public String toString(){
		String s = name().toLowerCase();
		s = s.replace("_", "-");
		s = s.replace("$", "@");
		return s;
	}
	
